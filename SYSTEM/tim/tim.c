void TIM3_IRQHandler(void)   //TIM3中断
{
	if(TIM_GetITStatus(TIM3, TIM_IT_Update) != RESET)  //检查TIM3更新中断发生与否
		{
		  dma_tim++;  
			mm++;
			
			usart1time1++;  //串口2发送 
			usart3time1++;
			rgbwtim++;
			cantim++;
			
		  if(USART1_TxOV>=1)usart1time2++; //发送完成延时
			if(USART3_TxOV>=1)usart3time2++; //发送完成延时 
			
			
			if(mm>=10)
      {
       mm=0;
	//   LED2=!LED2;	
			 rgbwytim++;
      }
				
			
			
			
						
		 TIM_ClearITPendingBit(TIM3, TIM_IT_Update  );  //清除TIMx更新中断标志 
	//	 
		}
}


